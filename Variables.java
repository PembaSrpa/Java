public class Variables {
    public static void main(String[] args){
        int num; // Declare an integer variable named num.
        num = 99; // Assign the value 99 to num.

        int no = 66; // Declare and initialize an integer variable named no to 66.

        short myShort = 835; // Declare and initialize a short variable named myShort to 835.

        long mylong = 672135; // Declare and initialize a long variable named mylong to 672135.

        double mydouble = 3.1234; // Declare and initialize a double variable named mydouble to 3.1234.

        float myfloat = 2.25f; // Declare and initialize a float variable named myfloat to 2.25.  //Hint: Note the 'f' suffix for float literals

        char mychar = 'a'; // Declare and initialize a char variable named mychar to 'a'.

        boolean mybool = true; // Declare and initialize a boolean variable named mybool to true.

        byte mybyte = 123; // Declare and initialize a byte variable named mybyte to 123.

        System.out.println(num); // Prints the value of num.
        System.out.println(no);  // Prints the value of no.
        System.out.println(myShort); // Prints the value of myShort.
        System.out.println(mylong); // Prints the value of mylong.
        System.out.println(mydouble); // Prints the value of mydouble.
        System.out.println(myfloat); // Prints the value of myfloat.
        System.out.println(mychar); // Prints the value of mychar.
        System.out.println(mybool); // Prints the value of mybool.
        System.out.println(mybyte); // Prints the value of mybyte.
    }
}
