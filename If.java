public class If {
    public static void main(String[] args) {
        int num = 3; // Initialize num to 3

        // if-else-if statement to check the value of num.
        if (num == 4){ // Checks if num is equal to 4.
            System.out.println("1"); // Executes if num is 4.
        }
        else if (num == 3) { // Checks if num is equal to 3.
            System.out.println("2"); // Executes if num is 3.
        }
        else{ // Executes if num is neither 4 nor 3.
            System.out.println("3");
        }
    }
}
